function neighborCount(x,y) {
    var c=0; /* number of living neighbors */
    if (cells[x-1][y] == 1)
        c++; /* or c=c+1 */
    if (cells[x+1][y] == 1)
        c++; /* or c=c+1 */
    if (cells[x][y-1] == 1)
        c++; /* or c=c+1 */
    if (cells[x][y+1] == 1)
        c++; /* or c=c+1 */
    if (cells[x-1][y-1] == 1)
        c++; /* or c=c+1 */
    if (cells[x+1][y+1] == 1)
        c++; /* or c=c+1 */
    if (cells[x-1][y+1] == 1)
        c++; /* or c=c+1 */
    if (cells[x+1][y-1] == 1)
        c++; /* or c=c+1 */
    return c;
}

function draw() {
    ctx.globalCompositeOperation = 'destination-over';
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    for(var i=1;i<(grid_size-1);i=i+1)
        for(var j=1;j<(grid_size-1);j=j+1) {
            if (cells[i][j] == 0)
                ctx.fillStyle = "rgba(200,200,200,255)";
            else
                ctx.fillStyle = "rgba(0,0,255,255)";
            ctx.fillRect(x+(i*11), y+(j*11), 10, 10);

            newCells[i][j] = 0 // Every cell is dead by default
            if (neighborCount(i, j) == 3) // three neighbors and you're alive
                newCells[i][j] = 1
            else if (neighborCount(i, j) == 2) // if two neighbors, then...
                if (cells[i][j] == 1) // you're only alive if you already were
                    newCells[i][j] = 1
        }
    for(var i=1;i<(grid_size-1);i=i+1)
        for(var j=1;j<(grid_size-1);j=j+1)
            cells[i][j] = newCells[i][j]
    setTimeout(function() {
        window.requestAnimationFrame(draw);
    }, 1000 / framesPerSecond);
//    window.requestAnimationFrame(draw);
}

